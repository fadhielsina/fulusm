			    <p align="center"><b>FAQ</b></p>
<p align="justify">Di Fulusme, jika memiliki dana lebih, daripada menganggur, bisa dijadikan alternatif untuk memutar uang sehingga bisa memberi nilai tambah bagi investor. Sebaliknya, jika memiliki banyak proyek tetapi terkendala oleh modal, dapat meminjam untuk periode tertentu, sehingga perusahaan hanya fokus untuk mencari proyek saja dan tidak perlu dipusingkan dalam mencari modal</p>
<p><b>Apakah model bisnis Fulusme ?</b></p>
<p align="justify">Fulusme adalah platform Peer-to-peer lending (P2P lending). P2P Lending adalah salah satu wadah tempat bertemunya pendana dan peminjam yang dihubungkan dengan teknologi aplikasi, sehingga berikutnya disebut teknologi finansial (Financial Technology/ FinTech).</p>
<p><b>Karena Fulusme berbasis syariah, apakah hanya umat muslim saja yang boleh meminjam?</b></p>
<p align="justify">Tentu saja tidak. semua lapisan, ras dan lintas agama bebas untuk menjadi pendana maupun peminjam, sebagaimana bank – bank syariah yang makin berkembang di negara eropa dan amerika yang customernya tidak hanya muslim namun juga non muslim</p>
<p><b>Apakah Fulusme sama dengan fungsi bank?</b></p>
<p align="justify">Tidak, karena Fulusme hanya penyaluran pinjaman yang di danai oleh investor untuk project yang spesifik saja dan tidak ada proses penyimpanan uang seperti bank. Fulusme bisa dijadikan alternatif bisnis yang menguntungkan, karena selain aman, juga memiliki return yang menarik dalam periode yang cepat/pendek. </p>
<p><b>Jika bukan bank, apakah aman berinvestasi di Fulusme?</b></p>
<p align="justify">Meskipun Fulusme bukan bank, tetapi tetap memiliki sistem yang ketat dalam melakukan verifikasi project sebelum di danai. kami juga memilki tim yang melakukan analisa resiko yang didasarkan atas hasil kompilasi dari verifikasi dan inteligent data yang canggih untuk memastikan bahwa project yang akan di danai adalah valid dan menguntungkan bagi investor. Selain itu, Fulusme juga diawasi dan terdaftar di OJK (otoritas Jasa Keuangan) sehingga menjamin uangcepat telah memenuhi kaidah keamanan bagi para investor</p>
<p><b>Apakah mungkin terjadi gagal bayar (default) ?</b></p>
<p align="justify">Segala kemungkinan bisa saja terjadi pada setiap alternatif model investasi. Sedari awal, delafintech berusaha me-mitigasi segala resiko yang mungkin timbul. Fulusme sedapat mungkin juga melakukan screening yang ketat sebelum memberikan permodalan. jika terjadi tanda-tanda potensi gagal bayar, Fulusme akan melakukan pendekatan kepada peminjam untuk melakukan kewajibannya. Jika masih belum berhasil, maka Fulusme akan bekerjasama dengan pihak ketiga untuk melakukan penagihan.</p>
<p><b>Berapa lama waktu peminjaman di Fulusme?</b></p>
<p align="justify">Berkisar dari 1 bulan hingga 6 bulan</p>
<p><b>Apakah Fulusme dapat membantu semua kebutuhan permodalan?</b></p>
<p align="justify">Ya, tetapi untuk saat ini hanya membiayai kebutuhan permodalan untuk bisnis (B2B) dan tidak perseorangan serta melalui proses asesmen tim terlebih dahulu.</p>
<p><b>Berapa besar pinjaman yang bisa di ajukan?</b></p>
<p align="justify">Peminjaman bisa dimulai dari Rp 100 juta hingga maksimal Rp 2 milyar</p>
<br>
<p align="center"><b>FAQ Pendanaan :</b></p>

<p><b>Apa syarat untuk memberikan pendanaan/pemberi pinjaman/Investor?</b></p>
	<p>Tidak ada syarat khusus. Minimal berusia 18 tahun. WNA dan WNI juga berhak untuk menjadi pendana. Memiliki KTP/identitas diri yang masih berlaku</p>
<p><b>Bagaimana proses untuk menjadi pendana?</b></p>
	<p>Pertama kali harus mendaftar di bagian registrasi pendanaan. Isi data-data yang diminta, kemudian klik “daftar”. 
	Selanjutnya periksa email anda dan klik link verifikasi yang dikirimkan Fulusme, untuk memastikan bahwa email itu adalah benar milik anda
	Berikutnya anda akan memiliki akun pribadi di Fulusme. Di akun itu anda dapat melihat semua profile proyek investasi anda</p>
<p><b>Bagaimana untuk melihat proyek yang akan di danai?</b></p>
	<p>Anda dapat melihat dan memilih proyek investasi di akun pribadi anda</p>
<p><b>Apakah bisa mendanai beberapa proyek dalam satu waktu?</b></p>
	<p>Ya, anda bisa mendanai beberapa proyek dalam waktu yang bersamaan</p>
<p><b>Bagaimana nilai perhitungan keuntungan investasi pemberi pinjaman?</b></p>
	<p>Pada saat anda membuka marketplace, maka akan keluar nilai rupiah keuntungan anda yang didapat dari nilai bagi hasil dengan peminjam yang besarnya berdasarkan rasio scoring resiko yang telah dihitung oleh team Fulusme</p>
<p><b>Bagaimana cara melihat profile pembayaran proyek?</b></p>
	<p>Anda dapat melihat laporan bulanan lengkap di akun pribadi anda. Jika pendanaan anda dimulai sebelum tanggal 15, maka anda akan melihat laporan terbarunya setiap tanggal 15 di bulan berikutnya. Dan jika pendanaan anda dimulai setelah tanggal 15, maka laporan terbarunya setiap tanggal 1 di bulan baru</p>
	<br>
	<p align="center"><b>FAQ Peminjam :</b></p>

<p><b>Siapa saja yang berhak sebagai peminjam ?</b></p>
	<p>Semua perusahaan yang memiliki badan hukum di wilayah negara Republik Indonesia
	Badan hukum perusahaan berbentuk PT
	Domisili PT di sekitar Jabodetabek dan sekitar Bandung
	Telah beroperasi minimal 1 tahun
	Memiliki dokumen legal perusahaan, rekening koran, NPWP dll
</p>
<p><b>Bagaimana proses peminjaman?</b></p>
	<ul>
	    <li><p>Pertama kali harus mendaftar di bagian registrasi Pinjaman. Isi data-data yang diminta, kemudian klik “daftar”.</p></li> 
	<li><p>Selanjutnya periksa email anda dan klik link verifikasi yang dikirimkan Fulusme, untuk memastikan bahwa email itu adalah benar milik anda</p></li>
	<li><p>Berikutnya anda akan memiliki akun pribadi di Fulusme.</p></li>
	</ul>
<p><b>Apa saja syarat untuk menjadi peminjam ?</b></p>
<ul>
    <li><p>Mengisi form pinjaman</p></li> 
	<li><p>Melengkapi dokumen penjamin yang dibutuhkan, seperti KTP/SIM</p></li>
	<li><p>Melengkapi dokumen perusahaan seperti SIUP</p></li>
	<li><p>Melengkapi dokumen proyek seperti SPK/Invoice dan dokumen pendukung lainnya, jika ada</p></li>
</ul>
<p><b>Berapa lama proses dari peminjaman hingga pencairan dana?</b></p>
	<p>Sejak dari form pinjaman di submit, maka agen kami akan segera mengunjungi perusahaan anda untuk melakukan pencocokan data dan verifikasi
	Jika sudah lolos verifikasi, maka anda akan mendapatkan informasinya via email/akun anda yang ada di Fulusme
	Sejak informasi anda diterima, maka segera kami akan melakukan crowdfunding selama 2 hari untuk pengumpulan dana yang dibutuhkan. Jika sudah mencukupi, maka di hari ke 4 kami akan mentransfernya. </p>
<p><b>Apakah ada fee/biaya untuk peminjaman? </b></p>
<ul>
<li><p>Karena Fulusme berbasis syariah, maka pada saat melakukan verifikasi onsite ke perusahaan calon peminjam, akan ada pertanyaan, berapa nilai estimasi keuntungan yang dihasilkan dari nilai pinjaman</p></li>
<li><p>Nilai keuntungan tersebut nantinya akan dijadikan patokan total keuntungan yang dibagi hasil antara peminjam dan pendana berdasarkan skoring tingkat resikonya</p></li>
<li><p>Pihak marketplace, dalam hal ini Fulusme akan mengutip biaya 3% dari total pinjaman yang disetujui, yang akan dipotong setelah projeknya selesai.</p></li>
<li><p>Meskipun rasio bagi hasil antara peminjam dan pendana bervariasi sesuai skoring tingkat resiko, namun nilai maximum antara pendana dan peminjam adalah 60:40</p></li>
</ul>

<p><b>Apakah bisa meminjam lagi pada saat pinjaman sedang berjalan?</b></p>
	<p>Ya, anda tetap bisa meminjam tetapi tetap dengan prosedur dan verifikasi sebagaimana layaknya pinjaman baru</p>




				
				
