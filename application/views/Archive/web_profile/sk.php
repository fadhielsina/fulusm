<p><b>Syarat dan Ketentuan Fulusme</b></p>
<br>
<p align="center"><b>Umum</b></p>
<ol type="1">
<li><p>Judul yang digunakan dalam S&K ini adalah dalam bentuk notifikasi dan tidak untuk mempengaruhi dan mengubah penafsiran</p></li>
<li><p>Jika terdapat perbedaan antara akad perjanjian dan S&K, maka yang berlaku adalah akad perjanjian</p></li>
<li><p>S&K ini bisa direvisi maupun di amandemen sesuai dengan kebutuhan dan kemudian disetujui para stackholder</p></li>
</ol>
<br>
<p align="center"><b>Definisi</b></p>
<ol type="1">
<li><p>Fulusme adalah penyedia layanan peer to peer lending, yaitu tempat bertemunya Pendana (lender) dan Peminjam (borrower) dalam 1 marketplace (platform)</p></li>
<li><p>Fulusme berikutnya didefinisikan sebagai Platform atau penyedia layanan</p></li>
<li><p>Fulusme hanya terdaftar sebagai platform peer to peer lending dan tidak menjalankan kegiatan perbankan, pasar modal, asuransi atau yang setara dan sejenisnya</p></li>
<li><p>Fulusme bukan nama perusahaan melainkan merk dagang dari PT Dinamika Niaga Ahyana yang berlokasi di Jakarta</p></li>
<li><p>Yang dimaksud dengan Pendana adalah entiti atau perseorangan yang menginvestasikan uangnya didalam suatu project dalam kurun waktu tertentu berdasarkan informasi profil yang disajikan pihak Fulusme</p></li>
<li><p>Yang dimaksud dengan Peminjam adalah badan usaha yang memiliki dokumen legal diwilayah NKRI yang ditandasahkan oleh badan yang berwenang</p></li>
<li><p>Yang dimaksud dengan stackholder pada S&K ini adalah para pemangku kepentingan atau mitra bisnis bagi platform, yaitu pihak Peminjam dan Pendana, dan tidak terkait maksudnya dengan para share holder di internal platform</p></li>
</ol>
<br>

<p align="center"><b>Komisi dan atau bagi hasil</b></p>
<ol type="1">
<li><p>Yang dimaksud dengan return pinjaman adalah nilai tambah dari angka modal pokok yang dipinjamkan dari pihak pendana (investor) kepada pemilik project. Nilai ini bisa berarti keuntungan atau bagi hasil untuk Pendana </p></li>
<li><p>Platform dalam kapasitasnya memberikan layanan marketplace, akan mengutip komisi yang besarnya 3% dari total pinjaman yang disetujui</p></li>
<li><p>Dengan pertimbangan tertentu dari pihak Platform sehingga calon Peminjam dan Pendana tidak berhak dalam daftar stackholder di Fulusme, maka pihak Platform tidak akan membebankan kewajiban atau biaya apapun </p></li>
</ol>
<br>
<p align="center"><b>Persetujuan pinjaman</b></p>
<ol type="1">
<li><p>Platform berkewajiban mencari dan memberikan profil informasi yang detil dan jelas tentang project yang diajukan oleh calon Peminjam, kepada Pendana</p></li>
<li><p>Project yang diputuskan akan didanai oleh Pendana, merupakan tanggung jawab Pendana sepenuhnya.</p></li> 
<li><p>Dalam proses analisa dan kompilasi data berdasarkan hasil survey dan pengajuan via web, platform memiliki pertimbangan absolut untuk menentukan apakah calon masuk dalam daftar Peminjam atau pendana</p></li>
</ol>
<br>
<p align="center"><b>Data Personal</b></p>
<ol type="1">
<li><p>Didalam proses verifikasi data, pihak Platform berhak untuk memproses, menganalisa semua informasi data personal mengenai calon Peminjam maupun Pendana, baik yang langsung berasal dari stackholder maupun yang didapat dari sumber lain sesuai dengan hukum yang berlaku</p></li>
<li><p>Fulusme akan merahasiakan semua data personal dan tidak menggunakan data tersebut untuk maksud lain selain untuk proses pemberian fasilitas Pinjaman </p></li>
</ol>
<p align="center"><b>Default</b></p>
<ol type="1">
<li><p>Pada situasi Peminjam berpotensi gagal bayar, Fulusme akan memberikan peringatan kepada Peminjam dalam jangka waktu 5 (lima) Hari Kerja. Jika dalam tempo tersebut tidak diindahkan, maka Peminjam dinyatakan wanprestasi. Jika ini terjadi, maka Fulusme akan melakukan pendekatan secara manusiawi supaya peminjam dapat menyelesaikan kewajiban yang seharusnya kepada pendana  </p></li> 
</ol>
				
